// Check if a number is odd or even in JavaScript


// What is Even Number

// Even Number  are numbers that can be divided by 2 without any remainder.


// What is Odd Number


// These are numbers that cannot be divided by 2 without having a remainder of 1.

// Example

// 12 % 2 = 0 Even Number  
// 13 % 2 = 1 Odd Number  

let number = 4;

if(number % 2 === 0){
    console.log(`${number} is Even Number`)
}else{
    console.log(`${number} is Odd Number`)
}