#include <stdio.h>

// This is the main function where the execution starts
int main() {
    int a = 5; // Initialize variable 'a' with the value 5

    /*
     * The following block of code calculates the square of the number 'a'
     * and prints the result to the console.
     */
    int square = a * a; // Calculate the square of 'a'

    // Print the result to the console
    printf("The square of %d is %d\n", a, square);

    return 0; // End of the program
}
