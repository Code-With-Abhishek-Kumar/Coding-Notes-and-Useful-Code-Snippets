
// Here we Store Multiple data in a Single Variable using Array

// 'users' is an array containing names of users
let users = ["Manish", "Rohan", "Ram", "Abhishek"];


console.log(users)

// Creating an array to store list of fruits
let fruits = ["Apple", "Banana", "Orange", "Mango"];

// Accessing elements in the array using index
let firstFruit = fruits[0]; // Retrieves the first element "Apple" 
let thirdFruit = fruits[2]; // Retrieves the third element "Orange"

// Modifying an element in the array
fruits[1] = "Grapes"; // Changes the second element "Banana" to "Grapes"

// Adding a new element to the end of the array
fruits.push("Pineapple"); // Adds "Pineapple" to the end of the array

// Logging the array to the console
console.log("List of fruits:", fruits);

// Accessing and logging specific elements
console.log("First fruit:", firstFruit);
console.log("Third fruit:", thirdFruit);


