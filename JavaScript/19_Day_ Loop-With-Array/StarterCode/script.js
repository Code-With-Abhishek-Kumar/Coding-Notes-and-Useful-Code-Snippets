// Declare an array with numbers from 1 to 7
let arr = [1, 2, 3, 4, 5, 6, 7];

// Use the forEach method to iterate over each element in the array
arr.forEach(function(element) {
    // Inside the forEach callback function, 'element' represents the current item in the array
    console.log(element);  // Print the current element to the console
});

